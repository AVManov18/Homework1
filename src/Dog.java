public class Dog {

    private String gender;
    private int year;
    private String name;



    public void setGender(String gender) {
        this.gender = gender;
    }
    public String getGender() {
        return gender;
    }


    public void setAge(int year) {
        this.year = year;
    }
    public int getAge() {
        return year;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

}